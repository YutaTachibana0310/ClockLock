/******************************************************************************
初期化処理
******************************************************************************/
HRESULT InitEffect(int num)
{
	InitEffectFire(num);
	InitEffectExplosion(num);
	InitEffectPiece(num);
	InitEnemyParticle(num);
	InitPlayerParticle(num);
	return S_OK;
}

/***************************************************************************
終了処理
//***************************************************************************/
void UninitEffect(void)
{
	UninitEffectFire();
	UninitEffectExplosion();
	UninitEffectPiece();
	UninitEnemyParticle();
	UninitPlayerParticle();
}

//***************************************************************************==
// 更新処理
//***************************************************************************==
void UpdateEffect(void)
{
	UpdateEffectFire();
	UpdateEffectExplosion();
	UpdateEffectPiece();
	UpdateEnemyParticle();
	UpdatePlayerParticle();
}

//***************************************************************************==
// 描画処理
//***************************************************************************==
void DrawEffect(void)
{
	DrawEffectExplosion();
	DrawEffectFire();
	DrawEffectPiece();
}
